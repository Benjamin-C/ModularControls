import board
import neopixel
from adafruit_debouncer import Debouncer
import digitalio

# The pin that the pixels are connected to
pixel_pin = board.GP6
# The nubmer of pixels on the board
num_pixels = 16
# The order of the bytes to the LEDs
ORDER = neopixel.GRB

# left up  down  right 
# LUTs to allow all rotations of the keyboard having key 0 at the upper left
CABLE_LEFT_MAP  = (0x0, 0x1, 0x2, 0x3, 0x4, 0x5, 0x6, 0x7, 0x8, 0x9, 0xA, 0xB, 0xC, 0xD, 0xE, 0xF)
CABLE_DOWN_MAP  = (0xC, 0x8, 0x4, 0x0, 0xD, 0x9, 0x5, 0x1, 0xE, 0xA, 0x6, 0x2, 0xF, 0xB, 0x7, 0x3)
CABLE_UP_MAP    = (0x3, 0x7, 0xB, 0xF, 0x2, 0x6, 0xA, 0xE, 0x1, 0x5, 0x9, 0xD, 0x0, 0x4, 0x8, 0xC)
CABLE_RIGHT_MAP = (0xF, 0xE, 0xD, 0xC, 0xB, 0xA, 0x9, 0x8, 0x7, 0x6, 0x5, 0x4, 0x3, 0x2, 0x1, 0x0)

keyMapping = CABLE_LEFT_MAP

pixels = neopixel.NeoPixel(pixel_pin, num_pixels, brightness=0.1, auto_write=False, pixel_order=ORDER)

buttonPins = [board.GP2,  board.GP3,  board.GP4,  board.GP5,
              board.GP8,  board.GP9,  board.GP10, board.GP11,
              board.GP21, board.GP20, board.GP17, board.GP16,
              board.GP28, board.GP27, board.GP26, board.GP22]

